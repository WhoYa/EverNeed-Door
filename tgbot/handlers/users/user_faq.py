# tgbot/handlers/user_faq.py
from aiogram import Router, F
from aiogram.types import CallbackQuery
from aiogram.filters import Command
import logging

user_faq_router = Router()

@user_faq_router.callback_query(F.data == "faq")
async def show_faq(callback: CallbackQuery):
    """
    Отправляет список часто задаваемых вопросов и ответов.
    """
    faq_text = (
        "*Часто задаваемые вопросы:*\n\n"
        "1. *Какие виды дверей вы продаёте?*\n"
        "   - Мы предлагаем широкий ассортимент дверей, включая межкомнатные, входные, раздвижные и складные двери из различных материалов: дерева, стекла, МДФ и металла.\n\n"
        "2. *Какой срок доставки?*\n"
        "   - Срок доставки зависит от выбранного вами товара и вашего местоположения. Обычно доставка занимает от 3 до 7 рабочих дней.\n\n"
        "3. *Предоставляете ли вы гарантию на двери?*\n"
        "   - Да, на все наши двери предоставляется гарантия сроком на 12 месяцев с момента покупки.\n\n"
        "4. *Могу ли я заказать установку двери?*\n"
        "   - Конечно! Мы предоставляем услугу профессиональной установки дверей. При оформлении заказа вы можете выбрать этот вариант.\n\n"
        "5. *Как выбрать подходящую дверь для дома?*\n"
        "   - Рекомендуем учитывать стиль интерьера, материал, размеры и функциональные требования. Наши консультанты всегда готовы помочь вам с выбором.\n"
    )
    try:
        await callback.message.edit_text(
            faq_text,
            parse_mode="Markdown",
            reply_markup=None  # Убираем клавиатуру, если необходимо
        )
    except Exception as e:
        logging.error(f"Ошибка при отправке FAQ: {e}")
        await callback.answer("Произошла ошибка при отображении FAQ.", show_alert=True)
